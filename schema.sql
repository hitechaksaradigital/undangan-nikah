-- RSVP Table
create table rsvp (
  id bigint generated by default as identity primary key,
  name text not null,
  guest_count text not null default '1 Orang',
  attendance text not null,
  created_at timestamp with time zone default now()
);

-- Wishes Table
create table wishes (
  id bigint generated by default as identity primary key,
  name text not null,
  message text not null,
  created_at timestamp with time zone default now()
);

-- Enable RLS (Row Level Security)
alter table rsvp enable row level security;
alter table wishes enable row level security;

-- Policies - allow public insert and read
create policy "Allow public insert rsvp" on rsvp for insert to public with check (true);
create policy "Allow public select rsvp" on rsvp for select to public using (true);
create policy "Allow public insert wishes" on wishes for insert to public with check (true);
create policy "Allow public select wishes" on wishes for select to public using (true);